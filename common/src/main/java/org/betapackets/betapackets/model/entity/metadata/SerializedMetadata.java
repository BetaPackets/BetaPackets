package org.betapackets.betapackets.model.entity.metadata;

public class SerializedMetadata {

    private int index;
    private MetadataCodecType type;
    private Object value;

    public SerializedMetadata(int index, MetadataCodecType type, Object value) {
        this.index = index;
        this.type = type;
        this.value = value;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public MetadataCodecType getType() {
        return type;
    }

    public void setType(MetadataCodecType type) {
        this.type = type;
    }

    public Object getValue() {
        return value;
    }

    public void setValue(Object value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "SerializedMetadata{" +
                "index=" + index +
                ", type=" + type +
                ", value=" + value +
                '}';
    }
}
